package com.example.appausa.model;





public class TipoCuenta {



    private String nombre;

    private String descrip;


    public TipoCuenta() {
    }

    public TipoCuenta(String nombre) {
        this.nombre = nombre;
    }

    public TipoCuenta(String nombre, String descrip) {
        this.nombre = nombre;
        this.descrip = descrip;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDescrip() {
        return descrip;
    }

    public void setDescrip(String descrip) {
        this.descrip = descrip;
    }


    @Override
    public int hashCode() {
        int hash = 0;
        hash += (nombre != null ? nombre.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof TipoCuenta)) {
            return false;
        }
        TipoCuenta other = (TipoCuenta) object;
        if ((this.nombre == null && other.nombre != null) || (this.nombre != null && !this.nombre.equals(other.nombre))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "entidadesbanco.TipoCuenta[ nombre=" + nombre + " ]";
    }
    
}
